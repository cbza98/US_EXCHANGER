CREATE PROCEDURE OBTENER_EMPRESA_CAMBIO
AS 
SELECT 
E.RUC, E.NOMBRE ,
(SELECT DT.NUMERO FROM OPE_DETALLE_TABLA  DT WHERE CODIGO = 'MONEDA'  and DT.REFERENCIA2 = '1') as COD_MONEDA_SYS,
(SELECT DT.NOMBRE FROM OPE_DETALLE_TABLA  DT WHERE CODIGO = 'MONEDA'  and DT.REFERENCIA2 = '1') as NOMBRE_MONEDA_SYS
--(SELECT DT2.NOMBRE FROM OPE_DETALLE_TABLA  DT2 WHERE CODIGO = 'MONEDA' ) AS COD_MONEDA_SYS,
from OPE_EMPRESA E 

